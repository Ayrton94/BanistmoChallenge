package models;

public class TasasData {
    private String tasaInteres;
    private String periodicidad;
    private String capitalizacion;

    // Getters y Setters
    public String getTasaInteres() { return tasaInteres; }
    public void setTasaInteres(String tasaInteres) { this.tasaInteres = tasaInteres; }

    public String getPeriodicidad() { return periodicidad; }
    public void setPeriodicidad(String periodicidad) { this.periodicidad = periodicidad; }

    public String getCapitalizacion() { return capitalizacion; }
    public void setCapitalizacion(String capitalizacion) { this.capitalizacion = capitalizacion; }
}